void main() {
  int a = 10;
  int b = 3;

  print("a = $a, b = $b");

  // Addition
  print("Addition: a + b = ${a + b}");

  // Subtraction
  print("Subtraction: a - b = ${a - b}");

  // Multiplication
  print("Multiplication: a * b = ${a * b}");

  // Division
  print("Division: a / b = ${a / b}");

  // Integer Division
  print("Integer Division: a ~/ b = ${a ~/ b}");

  // Modulus
  print("Modulus: a % b = ${a % b}");
}
