void main() {
  //string to int
  String s1 = "123";
  int n1 = int.parse(s1);
  print("String to int : $n1");

  //int to string
  int n2 = 456;
  String s2 = n2.toString();
  print("Int to String : $s2");

  //String to double
  String s3 = "23.5";
  double d1 = double.parse(s3);
  print("String to double : $d1");

  //double to string

  //int to double

  //double to int
}


//output
// String to int : 123
// Int to String : 456
// String to double : 23.5